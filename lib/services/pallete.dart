import 'package:flutter/material.dart';

class Pallete {
  static get black {
    return Colors.black;
  }

  static get white {
    return Colors.white;
  }

  static get primary {
    return const Color(0xFF457FFA);
  }

  static get secondary {
    return const Color(0xFFD2DFFF);
  }

  static get accent {
    return const Color(0xFF9AB6FF);
  }
}
